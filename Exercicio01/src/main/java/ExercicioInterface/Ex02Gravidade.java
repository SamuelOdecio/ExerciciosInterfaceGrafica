    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        double peso = Integer.parseInt(jTextField2.getText());
        double planeta = 0;
        
        double mercurio = 0.37;
        double venus = 0.88;
        double marte = 0.38;
        double jupiter = 2.64;
        double saturno = 1.15;
        double urano = 1.17;
        boolean buttonSelected = jRadioButton1.isSelected();
        if (buttonSelected == true) {
            planeta = peso * mercurio;
            
            JOptionPane.showMessageDialog(rootPane, "O seu peso em Mercúrio é: " + planeta);
        }

        buttonSelected = jRadioButton2.isSelected();
        if (buttonSelected == true) {
            planeta = peso * venus;
            JOptionPane.showMessageDialog(rootPane, "O seu peso em Vênus é: " + planeta);
        }

        buttonSelected = jRadioButton3.isSelected();
        if (buttonSelected == true) {
            planeta = peso * marte;
            JOptionPane.showMessageDialog(rootPane, "O seu peso em Marte é: " + planeta);
        }

        buttonSelected = jRadioButton4.isSelected();
        if (buttonSelected == true) {
            planeta = peso * jupiter;
            JOptionPane.showMessageDialog(rootPane, "O seu peso em Júpiter é: " + planeta);
        }

        buttonSelected = jRadioButton5.isSelected();
        if (buttonSelected == true) {
            planeta = peso * saturno;
            JOptionPane.showMessageDialog(rootPane, "O seu peso em Saturno é: " + planeta);
        }

        buttonSelected = jRadioButton6.isSelected();
        if (buttonSelected == true) {
            planeta = peso * urano;
            JOptionPane.showMessageDialog(rootPane, "O seu peso em Urano é: " + planeta);
        }




    }//GEN-LAST:event_jButton1MouseClicked

